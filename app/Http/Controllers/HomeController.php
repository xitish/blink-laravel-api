     */
    public function index()
    {
        $user = User::orderby('created_at', 'desc')->withCount('books')->withCount('notes')->get();
        $usercount = $user->count();
        $book = Book::orderby('created_at', 'desc')->with('user')->get();
        $bcount = $book->count();
        $note = Note::orderby('created_at', 'desc')->with('user')->get();
        $ncount = $note->count();
        return view('welcome', ['users' => $user, 'ucount' => $usercount, 'books' => $book, 'bcount' => $bcount, 'notes' => $note, 'ncount' => $ncount]);
    }

